#include "ft_printf.h"

int	check_flag(char c)
{
	if (c == '-' || c == '0')
		return (1);
	return (0);
}

int	check_num(char c)
{
	if (c >= '1' && c <= '9')
		return (1);
	return (0);
}

int	check_dot(char c)
{
	if (c == '.')
		return (1);
	return (0);
}
